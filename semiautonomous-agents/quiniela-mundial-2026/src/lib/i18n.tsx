type LocaleCtx = {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, fallback?: string) => string;
};

const Ctx = createContext<LocaleCtx>({
  locale: "es",
  setLocale: () => {},
  t: (k, f) => f ?? k,
});

export function LocaleProvider({ children }: { children: ReactNode }) {
  // Default to Spanish to match SSR. Hydration reads localStorage on mount —
  // an EN user sees one frame of Spanish before flip, which is acceptable for
  // a fun pool.
  const [locale, setLocaleState] = useState<Locale>("es");

  useEffect(() => {
    try {
      const stored = window.localStorage.getItem(STORAGE_KEY);
      if (stored === "es" || stored === "en") setLocaleState(stored);
    } catch { /* private mode / SSR — stay on default */ }
  }, []);

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    try { window.localStorage.setItem(STORAGE_KEY, l); } catch { /* ignore */ }
  }, []);

  const t = useCallback((key: string, fallback?: string) => {
    const primary = MESSAGES[locale]?.[key];
    if (primary !== undefined) return primary;
    const es = MESSAGES.es?.[key];
    if (es !== undefined) return es;
    return fallback ?? key;
  }, [locale]);

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t]);

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useLocale() {
  return useContext(Ctx);
}

// Helper for callers that want the Intl date locale matching the current UI.
export function intlLocale(locale: Locale): string {
  return locale === "en" ? "en-US" : "es-MX";
}
